// array methods 
// push
let arr = [1, 2, 3, 4, 5];
arr.push(20);


// pop
let arr = [1, 2, 3, 4, 5];
arr.pop();

// shift -> removes starting val of array
let arr = [1,2,3,4,5];
arr.shift();

//unshift -> adds a value in starting
let arr = [1, 2, 3, 4, 5];
arr.unshift();

// spice -> removes value from between array
let arr = [1, 2, 3, 4, 5];
arr.spice(2,1);

// slice -> it creates a new array after removing the desired values 
let arr = [1, 2, 3, 4, 5];
let newarr = arr.slice(0,3);


